# While quando não se sabe quantas vezes vai executar 
contador = 1
while contador < 10:
  print(contador)
  contador += 1


# For quando sabe quantas vezes vai executar  
for numero in range(5):
  print(numero)

for numero in range(5, 10): # Exibe do 5 até 9
  print(numero)

# Range
print(range(5)) # vai do 0,1,2,3,4
